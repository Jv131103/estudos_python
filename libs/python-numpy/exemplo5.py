import numpy as np

# Operações matemáticas com numpy
a = np.array([1, 2, 3])

# Seno
b = np.sin(a)
print(f"Seno do array {a}: {b}")
print()

# Cosseno
c = np.cos(a)
print(f"Cosseno do array {a}: {c}")
print()

# Logaritmo
d = np.log(a)
print(f"Logaritmo do array {a}: {d}")
print()

# Exponencial
e = np.exp(a)
print(f"Exponencial do array {a}: {e}")
print()

# Raiz Quadrada
f = np.sqrt(a)
print(f"Raiz quadrada do array {a}: {f}")
print()
